package com.zhaopin.po;

public class Companys {
	private int companyid;
	private String companyname;
	private String companypwd;
	private String companyemail;
	private String companylogo;
	private String companybrief;
	private int companysize;
	private String companylocate;
	public int getCompanyid() {
		return companyid;
	}
	public void setCompanyid(int companyid) {
		this.companyid = companyid;
	}
	public String getCompanyname() {
		return companyname;
	}
	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}
	public String getCompanypwd() {
		return companypwd;
	}
	public void setCompanypwd(String companypwd) {
		this.companypwd = companypwd;
	}
	public String getCompanyemail() {
		return companyemail;
	}
	public void setCompanyemail(String companyemail) {
		this.companyemail = companyemail;
	}
	public String getCompanylogo() {
		return companylogo;
	}
	public void setCompanylogo(String companylogo) {
		this.companylogo = companylogo;
	}
	public String getCompanybrief() {
		return companybrief;
	}
	public void setCompanybrief(String companybrief) {
		this.companybrief = companybrief;
	}
	public int getCompanysize() {
		return companysize;
	}
	public void setCompanysize(int companysize) {
		this.companysize = companysize;
	}
	public String getCompanylocate() {
		return companylocate;
	}
	public void setCompanylocate(String companylocate) {
		this.companylocate = companylocate;
	}
	
}